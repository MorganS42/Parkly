package main;

public class Photo {
    String image;
    int x;
    int y;
    int page;
    String name;

    public Photo(String image, int x, int y, int page, String name) {
        this.image = image;
        this.x = x;
        this.y = y;
        this.page = page;
        this.name = name;
    }
}
